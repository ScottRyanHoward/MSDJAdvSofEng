    private boolean resultsLegalies()
    {
        boolean valid = true;
        
        return valid;
    }
    private boolean formLegalities()
    {
        boolean valid = true;
        String error_msg = "";
        try {
          Double.parseDouble(price.getText());
       }
       catch (NumberFormatException e) {     
           error_msg = "ENTER A VALID PRICE";
           valid = false;
       }
       try {
           Integer.parseInt(quantity.getText());
       }
       catch (NumberFormatException e) {  
           error_msg = "ENTER A VALID QUANTITY";
           valid = false;
       }
       if(0 == product_id.getText().length())
       {
          error_msg = "ENTER A VALID PRODUCT ID";
          valid = false;
       }
       else if(0 == product_name.getText().length())
       {
          error_msg = "ENTER A VALID PRODUCT NAME";
          valid = false;
       }
       else if(0 == description_area.getText().length())
       {
          error_msg = "ENTER A DESCRIPTION";
          valid = false;
       }
       
       if (!valid)
       {
           JOptionPane optionPane = new JOptionPane(error_msg, JOptionPane.ERROR_MESSAGE);    
           JDialog dialog = optionPane.createDialog("Failure");
           dialog.setAlwaysOnTop(true);
           dialog.setVisible(true);
       }
       return valid;
    }
    
