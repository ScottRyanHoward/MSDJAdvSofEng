    private void addItemButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_addItemButtonActionPerformed
    {//GEN-HEADEREND:event_addItemButtonActionPerformed
        if (formLegalities() && resultsLegalies())
        {       
          product.setPrice(Double.parseDouble(price.getText()));
          product.setQuantity(Integer.parseInt(quantity.getText()));
          product.setProductId(product_id.getText());
          product.setProductName(product_name.getText());
          product.setDescription(description_area.getText());
          product.setCategory(category_combo_box.getSelectedItem().toString());
          product.setCategory(size_combo_box.getSelectedItem().toString());
          this.setVisible(false);
        }
       
    }//GEN-LAST:event_addItemButtonActionPerformed

